/// Тип, который можно создать из словаря и опционального идентификатора (для маппинга из JSON/Obj-C).
protocol MappableFromDictionary {
    /// Создаёт экземпляр из словаря и опционального идентификатора.
    ///
    /// Используется при разборе ответа API (например, JSON): по переданному словарю и,
    /// при необходимости, по `id` собирается объект.
    /// При неверной структуре или отсутствующих полях возвращает `nil`.
    ///
    /// - Parameters:
    ///   - dict: Словарь с данными (например, фрагмент JSON).
    ///   - id: Опциональный идентификатор сущности; при необходимости подставляется или перезаписывает значение из `dict`.
    /// - Returns: Экземпляр типа или `nil`, если данные не удалось разобрать.
    static func from(_ dict: [String: Any], id: String?) -> Self?
}
